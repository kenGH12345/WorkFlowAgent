// ─── Config Generator ─────────────────────────────────────────────────────────

/**
 * Generates a workflow.config.js file from a detected tech profile.
 * @param {string} projectRoot
 * @param {object} profile
 * @param {string} projectName
 * @returns {string} Path of the written config file
 */
function generateConfigFromProfile(projectRoot, profile, projectName) {
  const skillsJson = JSON.stringify(profile.skills, null, 4)
    .replace(/"name"/g, 'name')
    .replace(/"description"/g, 'description')
    .replace(/"domains"/g, 'domains');

  const defaultSkillsJson = JSON.stringify(profile.defaultSkills, null, 4);
  const extList = profile.extensions.map(e => `'${e}'`).join(', ');
  const ignoreDirsList = profile.ignoreDirs.map(d => `'${d}'`).join(', ');

  const testCmd = profile.testCommand !== undefined ? profile.testCommand : null;
  const testCommandLine = testCmd
    ? `testCommand: '${testCmd}',`
    : `testCommand: null,  // TODO: set your test command (e.g. 'npm test', 'flutter test', 'pytest')`;

  const content = `/**
 * workflow.config.js – Project Workflow Configuration
 *
 * Auto-generated by: node workflow/init-project.js
 * Detected tech stack: ${profile.name}
 * Generated at: ${new Date().toISOString()}
 *
 * You can customise this file at any time.
 * Re-run: node workflow/init-project.js  to apply changes.
 *
 * NOTE: projectName, techStack, sourceExtensions, and ignoreDirs are
 * intentionally omitted. They are auto-detected at runtime by detectTechStack()
 * and do not need to be configured manually. If you want to override them,
 * uncomment and edit the overrides section below.
 */

'use strict';

module.exports = {
  // ─── Runtime-Detected (uncomment to override) ─────────────────────────────
  // These are auto-detected at init time. Only set them if the detection is wrong.
  // projectName: '${projectName}',
  // techStack: '${profile.techStack}',
  // sourceExtensions: [${extList}],
  // ignoreDirs: [${ignoreDirsList}],

  // ─── Code Graph ──────────────────────────────────────────────────────────────
  // CodeGraph auto-scans ALL supported languages (.js, .ts, .cs, .lua, .go, .py, .dart).
  // No extensions config needed. Use scopeDirs for large monorepos only.
  codeGraph: {
    scopeDirs: [],             // Large monorepo only: ['packages/core', 'lua']. Empty = full scan
  },

  // ─── Automated Verification Loop ─────────────────────────────────────────
  ${testCommandLine}

  autoFixLoop: {
    enabled: true,
    maxFixRounds: 2,
    failOnUnfixed: false,
  },

  // ─── Project Architecture Profile ─────────────────────────────────────────
  // Auto-populated by ProjectProfiler during init. Contains deep analysis of
  // frameworks, architecture patterns, data layer, communication, testing, etc.
  // Consumed by Architect/Developer agents for context-aware decisions.
  projectProfile: null,  // Will be populated by init-project.js → ProjectProfiler

  // ─── Built-in Skills ─────────────────────────────────────────────────────
  builtinSkills: ${skillsJson},

  // ─── Default Skills ───────────────────────────────────────────────────────
  defaultSkills: ${defaultSkillsJson},

  // ─── Classification Rules ─────────────────────────────────────────────────
  classificationRules: [],
};
`;

  const configPath = path.join(projectRoot, 'workflow.config.js');
  fs.writeFileSync(configPath, content, 'utf-8');
  return configPath;
}

